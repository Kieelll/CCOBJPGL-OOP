package model;

class drinks {

    private String taste;
    private int price;

    public String getTaste(){
        return this.taste;
    }
    public void setTaste(String taste){
        this.taste = taste;
    }

    public int getPrice(){
        return this.price;
    }
    public void setPrice(int price){
        this.price = price;
    }
}

